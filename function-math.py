#Using Function to Add 2 Values
def additionmath(addnumberone, addnumbertwo):
    print(addnumberone + addnumbertwo)
additionmath(7, 3)
#Using Function to Substract 2 Values
def minusmath(minusnumberone, minusnumbertwo):
    print(minusnumberone - minusnumbertwo)
minusmath(7, 3)
#Using Function to Multiply 2 Values
def multiplymath(multiplynumberone, multiplynumbertwo):
    print(multiplynumberone * multiplynumbertwo)
multiplymath(7, 3)
#Using Function to Divide 2 Values
def dividemath(dividefirstnumber, dividesecondnumber):
    print(round(dividefirstnumber / dividesecondnumber))
dividemath(8, 2)